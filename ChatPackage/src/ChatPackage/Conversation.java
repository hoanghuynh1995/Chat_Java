package ChatPackage;

import java.io.Serializable;

/**
 *
 * @author ASUS
 */
public class Conversation implements Serializable{
    int id;
    String name;

    public Conversation() {
    }

   

    public Conversation(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
